public class Loop {
    public static void main(String[] args) {
        //loop
        for(int i=0;i<111;i++){
            System.out.println("Hello world "+i);
        }

        //while
        int i=0;
        while(i<10){
            System.out.println(i);
            i++;
        }

        //do-while
        int j=0;
        do{
            System.out.println("do="+j);
            j++;
        }while(j<10);
    }
}
